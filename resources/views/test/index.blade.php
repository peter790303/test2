		<p style="text-align: left;"><span style="font-size: 12pt;">數位廣告發展至今，網路廣告會在各大網站刊登，但是這些廣告真的都出現在正確的地方嗎？</span></p>
<p><span style="font-size: 12pt;"> </span></p>
<p><span style="font-size: 12pt;">程序化購買已經是目前最常使用的採買方式，廣告主透過DSP進行廣告採買，廣告曝光在各大媒體中達到其宣傳目的，但廣告真的是出現在對的地方嗎？根據eMarketer的調查顯示，從2015年底</span><span style="font-size: 16px;">(表1)</span><span style="font-size: 16px;">至2</span><span style="font-size: 16px;">018年</span><span style="font-size: 16px;">中</span><span style="font-size: 16px;">(表2)</span><span style="font-size: 12pt;">廣告主最擔心的問題之一仍是”詐欺流量Fraud”。</span></p>
<p><span style="font-size: 12pt;"> <img src="{{ URL::asset('images/0b994614003483537d0e9b035df99e71.png') }}"></span></p>
<p><span style="font-size: 12pt;">(表1)</span></p>
<p> </p>
<p><span style="font-size: 12pt;"><img src="{{ URL::asset('images/c379e049bc3c965418d86e70c0788689.png') }}"></span></p>
<p><span style="font-size: 12pt;">(表2)</span></p>
<p><span style="font-size: 12pt;"> </span></p>
<p><span style="font-size: 12pt;">在IAS(Integral Ad Science)2017下半年的媒體質量報告中指出，程序化購買(programmatic buying)上採用優化方式，已有效降低8.2%廣告露出在有詐欺流量疑慮的網站上。 (圖1)</span></p>
<p><span style="font-size: 12pt;"> </span></p>
<p><span style="font-size: 12pt;"><img src="{{ URL::asset('images/2c71dc36ebe49112f2ebe4f5ad439272.jpg') }}"></span></p>
<p><span style="font-size: 12pt;">圖1(資料來源：IAS2017下半年東南亞、香港、台灣的媒體質量報告)</span></p>
<p><span style="font-size: 12pt;"> </span></p>
<p><span style="font-size: 12pt;">要如何將詐欺流量最小化，是廣告主的重點項目之一。降低詐欺流量的其中一個方式是使用pre bid targeting(註)的優化機制，域動的DSP導入國際知名<strong><span style="color: #ff0000;">第三方監測公司IAS(Integral Ad Science)</span></strong>的優化機制，讓廣告主直接在域動DSP的平台上操作。除了預防詐欺流量之外，同時也導入<span style="color: #ff0000;"><strong>預防品牌安全(Brand Safety)</strong></span>和<span style="color: #ff0000;"><strong>提升可視率(Viewability)</strong></span>的優化機制，提供廣告主有更安全且有效的投放選擇。</span></p>
<p><span style="font-size: 12pt;"> </span></p>
<p><span style="font-size: 12pt;">接下來看看域動先前的案例，在使用預防詐欺流量的優化機制之後的成效：</span></p>
<p><span style="font-size: 12pt;"><img src="{{ URL::asset('images/7c6c9eecac474e566e846fb795d7883e.jpg') }}"></span></p>
<p><span style="font-size: 12pt;">圖2(資料來源：IAS Intro)</span></p>
<p><span style="font-size: 12pt;"> </span></p>
<p><span style="font-size: 12pt;">同場加映：</span></p>
<p><span style="font-size: 12pt;">除了預防詐欺流量的優化機制之外，還整合了預防品牌安全(Brand Safety)和可視率(Viewability)的優化機制，下面就來看看分別的成效：</span></p>
<p><span style="font-size: 12pt;">品牌安全(Brand Safety)的部分，同一個廣告活動，使用預防品牌安全(Brand Safety)的優化機制之後，有品牌安全疑慮的廣告曝光率從3.2%下降到0.6%</span></p>
<p><span style="font-size: 12pt;"> </span></p>
<p><span style="font-size: 12pt;"><img src="{{ URL::asset('images/60093de8a2b3a78484ecffdfbc32291a.jpg') }}"></span></p>
<p><span style="font-size: 12pt;">圖3(資料來源：IAS Intro)</span></p>
<p><span style="font-size: 12pt;"> </span></p>
<p><span style="font-size: 12pt;">而Viewability的部分，使用可視率優化機制之後，可視率從54.6%提升到69.6%，提升了27%。</span></p>
<p> </p>
<p><span style="font-size: 12pt;"><img src="{{ URL::asset('images/c2f4ebcf90ffc858757bda07c2ce89c4.jpg') }}"></span></p>
<p><span style="font-size: 12pt;">圖4(資料來源：IAS Intro)</span></p>
<p><span style="font-size: 12pt;"> </span></p>
<p><span style="font-size: 12pt;">以上可知pre bid targeting的優化機制可以有效提升廣告露出的品質，域動也會持續增加更多服務，讓廣告主在有限的預算下提升成效，達到KPI，想要了解相關服務，請跟我們聯絡。</span></p>
<p><span style="font-size: 12pt;"> </span></p>
<p><span style="font-size: 12pt;">註：</span></p>
<p><span style="font-size: 12pt;">pre bid targeting：在投遞廣告前先進行篩選的動作，確認符合項目後才可進行廣告的投遞。</span></p>
